export function applyTranslations(lang) {
  const map = TRANSLATIONS[lang] || TRANSLATIONS.pt;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (map[key]) el.textContent = map[key];
  });
  // Translate placeholder attributes
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (map[key]) el.placeholder = map[key];
  });
  // Translate sidebar dataset labels (injected by dataset-labels.js)
  if (window._applyDatasetLabels) window._applyDatasetLabels(lang);
  // Translate "Tipos Históricos" legend header
  const legendSpan = document.querySelector('.legend-filter-header span');
  if (legendSpan) legendSpan.textContent = map.tipos_historicos || 'Tipos Históricos';
  if (map.title) document.title = map.title;
}

export function getEntityLabel(entidade) {
  if (!entidade) return '';
  const langField = `nome_${currentLang}`;
  const tituloField = `titulo_${currentLang}`;
  
  // Tentar nome_lang primeiro
  if (entidade[langField]) return entidade[langField];
  
  // Fallback para titulo_lang (usado em alguns datasets)
  if (entidade[tituloField]) return entidade[tituloField];
  
  // Fallback para nome ou titulo em português
  if (entidade.nome) return entidade.nome;
  if (entidade.titulo) return entidade.titulo;
  
  // Fallback direto para campos de idioma específico
  if (currentLang === 'en' && entidade.nome_en) return entidade.nome_en;
  if (currentLang === 'en' && entidade.titulo_en) return entidade.titulo_en;
  if (currentLang === 'es' && entidade.nome_es) return entidade.nome_es;
  if (currentLang === 'es' && entidade.titulo_es) return entidade.titulo_es;
  if (entidade.nome_en) return entidade.nome_en;
  if (entidade.nome_es) return entidade.nome_es;
  return entidade.id || '';
}

export function getEntityField(entidade, fieldBase) {
  if (!entidade) return '';
  const langField = `${fieldBase}_${currentLang}`;
  if (entidade[langField]) return entidade[langField];
  if (entidade[fieldBase]) return entidade[fieldBase];
  if (entidade[`${fieldBase}_en`]) return entidade[`${fieldBase}_en`];
  if (entidade[`${fieldBase}_es`]) return entidade[`${fieldBase}_es`];
  return '';
}

export function paraRomano(num) {
  if (num < 1 || num > 3999) return num.toString();
  const romanos = [ ['M',1000], ['CM',900], ['D',500], ['CD',400], ['C',100], ['XC',90], ['L',50], ['XL',40], ['X',10], ['IX',9], ['V',5], ['IV',4], ['I',1] ];
  let resultado = '';
  for (const [letra, valor] of romanos) {
    while (num >= valor) { resultado += letra; num -= valor; }
  }
  return resultado;
}

export function formatarAno(ano) {
  const map = TRANSLATIONS[currentLang] || TRANSLATIONS.pt;
  const isBC = ano <= 0;
  const anoAbs = Math.abs(ano);
  const seculo = Math.ceil(anoAbs / 100);
  const roman = paraRomano(seculo);
  const suffix = (function(){ if (currentLang === 'en') return isBC ? 'BC' : 'AD'; return isBC ? 'AC' : 'DC'; })();
  const seculoLabel = map.seculo || 'Século';
  return `${anoAbs} ${suffix} (${seculoLabel} ${roman})`;
}
